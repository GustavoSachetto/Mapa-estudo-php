<?php

include 'variables.php';

/* ========= OPERADORES ATRIBUIÇÃO ========== */

// ((( RECEBE )))

/**
 * Esse operador atribui um valor a uma variável.
 */

$result = $num1;

echo $result;

// ((( ADICIONAR )))

/**
 * Esse operador adiciona o valor da variável atual com o valor da variável desejada.
 * Igual á: 
 * 
 * $result = $result + $num1.
 */

$result += $num1;

echo $result;

// ((( SUBTRAI )))

/**
 * Esse operador subtrai o valor da variável atual com o valor da variável desejada.
 * Igual á: 
 * 
 * $result = $result - $num1.
 */

$result -= $num1;

echo $result;

// ((( MULTIPLICA )))

/**
 * Esse operador multiplica o valor da variável atual com o valor da variável desejada.
 * Igual á 
 * 
 * $result = $result * $num1.
 */

$result *= $num1;

echo $result;

// ((( DIVIDE )))

/**
 * Esse operador divide o valor da variável atual com o valor da variável desejada.
 * Igual á: 
 * 
 * $result = $result / $num1.
 */

$result /= $num1;

echo $result;

// ((( MÓDULO )))

/**
 * Esse operador modula o resto da divisão do valor da variável atual com o valor da variável desejada.
 * Igual á: 
 * 
 * $result = $result % $num1.
 */

$result %= $num1;

echo $result;

// ((( CONCATENA )))

/**
 * Esse operador concatena o valor da variável atual com o valor da variável desejada.
 * Igual á:
 * 
 * $result = $result / $num1.
 */

$result .= $num1;

echo $result;

// ((( EXPONENCIAÇÃO )))

/**
 * Esse operador eleva o valor da variável atual com o valor da variável desejada.
 * Igual á:
 * 
 * $result = $result ** $num1.
 */

$result **= $num1;

echo $result;

/* ========================================== */

?>